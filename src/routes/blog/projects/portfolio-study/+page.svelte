			<h2>Introduction</h2>
			<p>
				In this case study, I will provide an in-depth analysis of the design process behind my
				personal portfolio, which highlights selected samples of my work, personal thoughts, and
				professional journey.
			</p>
			<p>
				This portfolio not only showcases my capabilities but also serves as a platform where I
				share my insights and creative ideas with a broader audience. Understanding the technologies
				and design choices I used will provide valuable insights into the complexity and creativity
				involved in developing a professional portfolio.
			</p>
			<h2>Overview</h2>
			<p>
				The development of my portfolio was segmented into three primary phases: wire-framing,
				initial design, and technology exploration and finalization.
			</p>
			<h2>Phase 1: Wire-Framing</h2>
			<p>
				Wire-framing served as the foundational stage of my portfolio's design process. This phase
				was crucial for laying out the structural framework of the site, ensuring that all necessary
				components were included and optimally positioned for user experience and navigation. I knew
				I wanted my website to not only show off my projects, but I also wanted it to be a blog.
			</p>
			<p>
				To achieve this, I used <a href="https://www.figma.com">Figma</a> to create a series of wireframes
				that outlined the layout of each page, including the homepage, project pages, and blog posts.
				This process allowed me to visualize the overall structure of the site and make informed decisions
				about the placement of key elements such as the navigation menu, project thumbnails, and blog
				post previews.
			</p>
			<p>
				Wire-framing also helped me identify potential usability issues and make adjustments to
				improve the overall user experience. For example, I realized that the initial layout of the
				project pages was too cluttered and overwhelming, so I reorganized the content to create a
				more streamlined and visually appealing design like the one you see now.
			</p>
			<p>
				By taking the time to wireframe my portfolio, I was able to establish a solid foundation for
				the design process and ensure that the final product would meet my goals and expectations.
			</p>

			<h2>Phase 2: Initial Design</h2>
			<p>
				With the wire-framing phase complete, I moved on to the initial design phase, where I began
				to bring my wireframes to life. This phase involved creating high-fidelity mockups of each
				page of my portfolio, using the wireframes as a guide for the layout and content.
			</p>
			<p>
				I wanted my portfolio to have a clean, modern design that would showcase my work in a
				visually appealing way. To achieve this, I found <a href="https://picocss.com">Pico CSS</a>
				to be the perfect tool for creating a responsive and visually appealing design as fast as possible.
			</p>
			<p>
				Pico CSS specializes in creating beautiful, minimalistic designs that are easy to customize
				with classless HTML. This allowed me to focus on the content and layout of my portfolio
				without getting bogged down in complex CSS styling. While the initial design phase produced
				a visually appealing portfolio, I knew that the final phase would require more advanced
				framework to bring my vision to life.
			</p>
			<h3>Why Pico CSS didn't work for me</h3>
			<ol>
				<li>
					<strong>Complexity</strong>. While Pico CSS is great for creating simple, minimalistic
					designs, it lacks the advanced features and customization options that I needed to create
					a more complex portfolio.
				</li>
				<li>
					<strong>Highly opinionated</strong>. Pico CSS is highly opinionated in its design choices,
					which can make it difficult to customize the design to fit your specific needs.
				</li>
				<li>
					<strong>Performance</strong>. While Pico CSS is lightweight and fast to build, it can
					sometimes be slow to load and render on certain devices, which can impact the user
					experience.
				</li>
			</ol>
			<h2>Phase 3: Technology Exploration and Finalization</h2>
			<p>
				With the initial design phase complete, I moved on to the final phase of the design process:
				technology exploration and finalization. This phase involved exploring different
				technologies and frameworks to determine the best tools for bringing my portfolio to life.
			</p>
			<p>
				After evaluating several options, I decided to use <a href="https://svelte.dev">Svelte</a>
				as the primary framework for my portfolio. Svelte is a modern JavaScript framework that combines
				the best features of React and Vue, making it an ideal choice for building complex, interactive
				web applications.
			</p>
			<p>
				Svelte's unique approach to building web applications, which involves compiling components
				at build time rather than runtime, allows for faster load times and improved performance.
				This along side it's simple syntax and powerful features made it the perfect choice for
				building simple or complex web applications.
			</p>
			<p>
				In addition to Svelte, I also used <a href="https://tailwindcss.com">Tailwind CSS</a> to handle
				the styling of my portfolio. Tailwind CSS is a utility-first CSS framework that allows for rapid
				prototyping and easy customization of styles. By combining Svelte and Tailwind CSS, I was able
				to create a visually appealing and highly functional portfolio that met all of my design goals.
			</p>
			<p>
				Tailwind CSS, incombination with <a href="https://flowbite.com">Flowbite</a> made it easy to
				create a visually appealing and highly functional portfolio that met all of my design goals.
			</p>
			<p>
				Finally, I used <a href="https://cloudflare.com">Cloudflare Pages</a> to host my portfolio. Cloudflare
				Pages is a fast and secure platform for deploying static sites, which made it easy to deploy
				my portfolio and ensure that it was accessible to users around the world.
			</p>
			<p>
				The final phase of the design process also involved testing the portfolio on different
				devices and browsers to ensure that it was responsive and accessible to all users. By taking
				the time to explore different technologies and finalize the design of my portfolio, I was
				able to create a professional and visually appealing website that effectively showcases my
				work and creative ideas.
			</p>
			<h2>Conclusion</h2>
			<p>
				Crafting my personal portfolio was a challenging but rewarding experience that allowed me to
				showcase my work and creative ideas in a visually appealing and professional way. By
				following a structured design process that involved wire-framing, initial design, and
				technology exploration and finalization, I was able to create a portfolio that met all of my
				goals and expectations.
			</p>
			<p>
				I hope that this case study provides valuable insights into the design process behind
				creating a personal portfolio and inspires others to take on similar projects. By
				understanding the tools and technologies involved in developing a portfolio, designers can
				create visually appealing and highly functional websites that effectively showcase their
				work and creative ideas.
			</p>
